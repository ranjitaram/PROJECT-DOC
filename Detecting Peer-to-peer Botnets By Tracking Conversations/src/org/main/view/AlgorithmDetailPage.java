    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        try {
            // TODO add your handling code here:
            TblAlgorithm tblAlgorithm = new TblAlgorithm();
            tblAlgorithm.setTxtAbstract(DiscriptionTextArea.getText());
            tblAlgorithm.setTxtCategoryId(Integer.toString(categoryIdComboBox.getSelectedIndex()));
            tblAlgorithm.setTxtDoc(docTextField.getText());
            tblAlgorithm.setTxtPdf(pdfTextField.getText());
            tblAlgorithm.setTxtPpt(pptTextfield.getText());
            tblAlgorithm.setTxtAlgorithmName(algoNameTextField.getText());
            tblAlgorithm.setTxtProjectCategory(technologyTextField.getText());
            SuperConnection.saveEntity(tblAlgorithm);

            // remain technology and discription
        } catch (Exception ex) {
            Logger.getLogger(AlgorithmDetailPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SaveButtonActionPerformed

